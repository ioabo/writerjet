    public function activateUser($id)
    {
        $user = User::find($id);

        $status = "Registered";

        if ($user) {
            $user->registration_status = $status;
            $user->save();
        }
    }
    public function promoteUser($id)
    {
        $user = User::find($id);

        $type = "Administrator";

        if ($user) {
            $user->type = $type;
            $user->save();
        }
    }
    public function demoteUser($id)
    {
        $user = User::find($id);

        $type = "User";

        if ($user) {
            $user->type = $type;
            $user->save();
        }
    }
